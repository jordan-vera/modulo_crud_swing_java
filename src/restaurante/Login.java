    public void acceder(String usuario, String pass) {
        String cap = "", nombres = "";
        String sql = "SELECT * FROM usuario WHERE NICK='" + usuario + "' AND CLAVE='" + this.getMD5(pass) + "'";
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                cap = rs.getString("NICK");
                nombres = rs.getString("NOMBRES_USER");
            }
            if (cap.equals(usuario)) {
                this.setVisible(false);
                Panel panel = new Panel();
                panel.setVisible(true);
                panel.pack();
                //panel.setExtendedState(MAXIMIZED_BOTH);
                panel.setTitle("Administrador Conectado" + "  (" + nombres + ")");
            } else {
                JOptionPane.showMessageDialog(this, "No existe sus datos");
            }
        } catch (SQLException ex) {
            Logger.getLogger(this.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public static String getMD5(String input) {
        try {
            MessageDigest md = MessageDigest.getInstance("MD5");
            byte[] messageDigest = md.digest(input.getBytes());
            BigInteger number = new BigInteger(1, messageDigest);
            String hashtext = number.toString(16);

            while (hashtext.length() < 32) {
                hashtext = "0" + hashtext;
            }
            return hashtext;
        } catch (NoSuchAlgorithmException e) {
            throw new RuntimeException(e);
        }
    }

